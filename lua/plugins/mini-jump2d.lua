-- ============================
-- mini.jump2d: 快速跳轉
-- ============================
-- GitHub: https://github.com/echasnovski/mini.jump2d
-- 功能：按 s + 字母，快速跳到螢幕上任何位置

return {
    "echasnovski/mini.jump2d",
    version = false,
    config = function()
        local jump2d = require("mini.jump2d")

        jump2d.setup({
            -- ============================
            -- 跳轉方式（使用預設）
            -- ============================
            -- 使用預設的 spotter（自動偵測跳轉點）
            -- 可以在後面的 start() 中指定不同的 spotter

            -- ============================
            -- 標籤設定（跳轉標記）
            -- ============================
            -- 使用這些字母作為跳轉標記
            labels = "abcdefghijklmnopqrstuvwxyz",

            -- ============================
            -- 映射（快捷鍵）
            -- ============================
            mappings = {
                start_jumping = "",  -- 我們自己設定快捷鍵，這裡留空
            },

            -- ============================
            -- 視覺設定
            -- ============================
            -- 靜默模式
            silent = false,
        })

        -- ============================
        -- 快捷鍵設定
        -- ============================
        local keymap = vim.keymap

        -- s + 字母：快速跳轉到螢幕上的任何行
        keymap.set("n", "s", function()
            jump2d.start(jump2d.builtin_opts.line_start)
        end, { desc = "跳轉到行首" })

        -- S（大寫）：跳轉到單詞開頭
        keymap.set("n", "S", function()
            jump2d.start(jump2d.builtin_opts.word_start)
        end, { desc = "跳轉到單詞" })

        -- ============================
        -- 使用說明
        -- ============================
        -- 【基本使用】
        -- 1. 按 s（或 S）
        -- 2. 螢幕上會顯示跳轉標記（a, b, c, ...）
        -- 3. 按對應的字母就能跳到該位置
        -- 4. 按 Esc 取消
        --
        -- 【兩種模式】
        --
        -- s（小寫）- 跳到行首：
        --   適合快速跳到某一行
        --   每行開頭會顯示一個字母
        --   按該字母就跳過去
        --
        -- S（大寫）- 跳到單詞：
        --   適合跳到特定單詞
        --   每個單詞開頭會顯示字母
        --   更精確的跳轉
        --
        -- 【實際例子】
        --
        -- 例子 1：跳到某一行
        -- ┌─────────────────────────────┐
        -- │ a  function foo() {         │
        -- │ b      console.log("hi");   │
        -- │ c      return true;         │
        -- │ d  }                        │
        -- │ e                           │
        -- │ f  function bar() {         │
        -- └─────────────────────────────┘
        -- 按 s → 看到標記 → 按 f → 跳到 function bar() 那行
        --
        -- 例子 2：跳到特定單詞
        -- ┌─────────────────────────────┐
        -- │ const a userName = b "John";│
        -- └─────────────────────────────┘
        -- 按 S → 看到標記 → 按 b → 跳到 "John"
        --
        -- 【使用場景】
        --
        -- 場景 1：快速跳到函式定義
        --   螢幕上有 10 個函式
        --   按 s → 看到標記 → 按對應字母 → 立刻跳過去
        --   比用 jjjjj 或 /search 快多了
        --
        -- 場景 2：在長檔案中導航
        --   檔案有 200 行
        --   想跳到第 150 行附近
        --   按 s → 看到標記 → 跳過去
        --   不用數行號或捲動
        --
        -- 場景 3：在分割視窗間跳轉
        --   有 3 個分割視窗
        --   按 s → 所有視窗都顯示標記
        --   按字母就能跳到對應視窗的對應位置
        --
        -- 【進階技巧】
        --
        -- 技巧 1：配合 d/c/y 使用
        --   ds{標記}  - 刪除到跳轉位置
        --   cs{標記}  - 修改到跳轉位置
        --   ys{標記}  - 複製到跳轉位置
        --
        -- 技巧 2：兩次跳轉
        --   如果目標位置的字母不在視野內
        --   第一次按 s → 跳到接近的位置
        --   第二次按 s → 精確跳到目標
        --
        -- 技巧 3：Visual 模式
        --   在 Visual 模式按 s
        --   選取範圍會延伸到跳轉位置
        --
        -- 【與其他跳轉比較】
        --
        -- Vim 原生：
        --   /{pattern}  - 需要輸入搜尋模式
        --   f{char}     - 只能在當前行跳
        --   5j          - 需要計算行數
        --   gg50        - 需要知道行號
        --
        -- mini.jump2d：
        --   s → 看到標記 → 一個字母搞定！
        --
        -- 【注意】
        -- s 原本是「刪除字元並進入插入模式」（substitute）
        -- 如果你常用 s，可以改用其他快捷鍵：
        --   keymap.set("n", "gs", ...)  -- 用 gs 代替 s
        -- 或者你可以用 cl 代替原本的 s（功能相同）
        --
        -- 【快捷鍵總覽】
        -- s        : 跳到行首（顯示所有行的標記）
        -- S        : 跳到單詞（顯示所有單詞的標記）
        -- {標記}   : 跳到該位置
        -- Esc      : 取消跳轉
    end,
}
