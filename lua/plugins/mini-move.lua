-- ============================
-- mini.move: 移動行和區塊
-- ============================
-- GitHub: https://github.com/echasnovski/mini.move
-- 功能：用 Alt+hjkl 移動當前行或選取的區塊

return {
    "echasnovski/mini.move",
    version = false,
    config = function()
        require("mini.move").setup({
            -- ============================
            -- 快捷鍵設定
            -- ============================
            mappings = {
                -- 移動選取範圍（Visual 模式）
                left = "<A-h>",   -- Alt+h 向左移動
                right = "<A-l>",  -- Alt+l 向右移動
                down = "<A-j>",   -- Alt+j 向下移動
                up = "<A-k>",     -- Alt+k 向上移動

                -- 移動當前行（Normal 模式）
                line_left = "<A-h>",   -- Alt+h 向左縮排
                line_right = "<A-l>",  -- Alt+l 向右縮排
                line_down = "<A-j>",   -- Alt+j 向下移動
                line_up = "<A-k>",     -- Alt+k 向上移動
            },

            -- ============================
            -- 選項
            -- ============================
            options = {
                -- 移動時自動重新縮排
                reindent_linewise = true,
            },
        })

        -- ============================
        -- 使用說明
        -- ============================
        -- 【基本使用】
        --
        -- === Normal 模式（移動當前行）===
        -- Alt+j    : 將當前行向下移動
        -- Alt+k    : 將當前行向上移動
        -- Alt+h    : 將當前行向左縮排（減少縮排）
        -- Alt+l    : 將當前行向右縮排（增加縮排）
        --
        -- === Visual 模式（移動選取的區塊）===
        -- 1. 選取幾行（Visual 或 Visual Line 模式）
        -- 2. Alt+j : 將選取的行向下移動
        -- 3. Alt+k : 將選取的行向上移動
        -- 4. Alt+h : 將選取的行向左縮排
        -- 5. Alt+l : 將選取的行向右縮排
        --
        -- 【實際例子】
        --
        -- 例子 1：調整函式順序
        -- ┌─────────────────────────┐
        -- │ function foo() {        │ ← 游標在這裡
        -- │     return 1;           │
        -- │ }                       │
        -- │                         │
        -- │ function bar() {        │
        -- │     return 2;           │
        -- │ }                       │
        -- └─────────────────────────┘
        --
        -- 想把 foo 移到 bar 下面：
        -- 1. 游標在 function foo()
        -- 2. V3j（選取 foo 的 3 行）
        -- 3. Alt+j Alt+j Alt+j（向下移 3 次）
        --
        -- 結果：
        -- ┌─────────────────────────┐
        -- │ function bar() {        │
        -- │     return 2;           │
        -- │ }                       │
        -- │                         │
        -- │ function foo() {        │
        -- │     return 1;           │
        -- │ }                       │
        -- └─────────────────────────┘
        --
        -- 例子 2：調整縮排
        -- ┌─────────────────────────┐
        -- │ function test() {       │
        -- │ console.log("hi");      │ ← 縮排不對
        -- │     return true;        │
        -- │ }                       │
        -- └─────────────────────────┘
        --
        -- 游標在 console.log 那行，按 Alt+l：
        -- ┌─────────────────────────┐
        -- │ function test() {       │
        -- │     console.log("hi");  │ ← 修正了！
        -- │     return true;        │
        -- │ }                       │
        -- └─────────────────────────┘
        --
        -- 例子 3：重新排列陣列項目
        -- ┌─────────────────────────┐
        -- │ const items = [         │
        -- │     "apple",            │
        -- │     "banana",           │ ← 想移到最上面
        -- │     "orange",           │
        -- │ ];                      │
        -- └─────────────────────────┘
        --
        -- 游標在 banana，按 Alt+k：
        -- ┌─────────────────────────┐
        -- │ const items = [         │
        -- │     "banana",           │ ← 移上來了
        -- │     "apple",            │
        -- │     "orange",           │
        -- │ ];                      │
        -- └─────────────────────────┘
        --
        -- 【使用場景】
        --
        -- 場景 1：重新排列函式
        --   寫完程式後想調整函式順序
        --   不用剪下貼上，直接 Alt+j/k 移動
        --
        -- 場景 2：調整 import 順序
        --   import 語句想重新排序
        --   選取後用 Alt+j/k 調整
        --
        -- 場景 3：快速修正縮排
        --   複製程式碼後縮排不對
        --   用 Alt+h/l 快速調整
        --
        -- 場景 4：重構程式碼結構
        --   想把某段程式碼移到 if 語句內
        --   用 Alt+l 增加縮排
        --
        -- 【與傳統方式比較】
        --
        -- 傳統方式（移動行）：
        --   ddkP        - 剪下、上移、貼上（3 個按鍵）
        --   ddp         - 剪下、下移、貼上（3 個按鍵）
        --
        -- mini.move：
        --   Alt+k       - 上移（1 個按鍵）
        --   Alt+j       - 下移（1 個按鍵）
        --
        -- 傳統方式（調整縮排）：
        --   >>          - 增加縮排
        --   <<          - 減少縮排
        --
        -- mini.move：
        --   Alt+l       - 增加縮排（更直覺）
        --   Alt+h       - 減少縮排
        --
        -- 【進階技巧】
        --
        -- 技巧 1：連續移動
        --   按住 Alt，連續按 j 可以一直往下移
        --   不用鬆開 Alt 再按
        --
        -- 技巧 2：Visual Line 模式
        --   V3j 選取 3 行
        --   Alt+j 一次移動 3 行
        --   比一行一行移快多了
        --
        -- 技巧 3：配合 . 重複
        --   移動一次後，按 . 可以重複相同的移動
        --   （但直接按 Alt+j 更快）
        --
        -- 【注意事項】
        --
        -- 1. Alt 鍵在不同終端機可能有不同行為
        --    Windows Terminal / PowerShell: 通常正常
        --    某些終端可能需要設定才能使用 Alt
        --
        -- 2. 如果 Alt+hjkl 不工作，可能和終端或 vim-tmux-navigator 衝突
        --    可以改用其他快捷鍵：
        --    mappings = {
        --        left = "<M-h>",  -- 或用 <M-> 代替 <A->
        --        ...
        --    }
        --
        -- 3. 自動重新縮排（reindent_linewise）
        --    移動後會自動調整縮排，讓程式碼保持正確格式
        --
        -- 【快捷鍵總覽】
        --
        -- Normal 模式（移動當前行）：
        -- Alt+j    : 向下移動當前行
        -- Alt+k    : 向上移動當前行
        -- Alt+h    : 減少縮排
        -- Alt+l    : 增加縮排
        --
        -- Visual 模式（移動選取的區塊）：
        -- Alt+j    : 向下移動選取的行
        -- Alt+k    : 向上移動選取的行
        -- Alt+h    : 減少選取行的縮排
        -- Alt+l    : 增加選取行的縮排
    end,
}
