-- ============================
-- mini.pairs: 自動括號配對
-- ============================
-- GitHub: https://github.com/echasnovski/mini.nvim/blob/main/readmes/mini-pairs.md
-- 功能：自動插入成對的括號、引號、HTML 標籤等

return {
    "echasnovski/mini.pairs",
    version = "*",  -- 使用穩定版本
    config = function()
        require("mini.pairs").setup({
            -- ============================
            -- 配對規則設定
            -- ============================
            -- 預設已經包含常用的配對：
            -- ( ) [ ] { } " " ' ' ` `

            modes = {
                insert = true,   -- 在插入模式啟用
                command = false, -- 在命令列模式不啟用（避免干擾命令輸入）
                terminal = false -- 在終端機模式不啟用
            },

            -- ============================
            -- 使用說明
            -- ============================
            -- 1. 輸入左括號 ( 會自動補上 ) 並將游標放在中間
            --    例如：輸入 ( → 自動變成 (|)  (| 表示游標位置)
            --
            -- 2. 如果你輸入右括號 ) 時已經有一個 )，會直接跳過，不會重複
            --    例如：(|) 時按 ) → ()|  (不會變成 ())|)
            --
            -- 3. 選取文字後按左括號，會用括號包圍選取的文字
            --    例如：選取 "hello" 後按 ( → 變成 (hello)
            --
            -- 4. 在空配對中按 Backspace 會同時刪除兩邊
            --    例如：(|) 按 Backspace → |  (兩個括號都刪除)
            --
            -- 5. 在空配對中按 Space 會在兩邊加空格
            --    例如：(|) 按 Space → ( | )
        })
    end,
}
